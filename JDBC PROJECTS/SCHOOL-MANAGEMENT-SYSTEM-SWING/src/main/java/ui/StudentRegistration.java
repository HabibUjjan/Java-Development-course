    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String name = jTextField1.getText();
    int age = Integer.parseInt(jTextField2.getText());
    String studentClass = jComboBox1.getSelectedItem().toString(); // âœ…
    String rollNo = jTextField3.getText();
    String gender=null;
    if(jRadioButton1.isSelected()){
          gender = jRadioButton1.getText();
      }
      if(jRadioButton2.isSelected()){
          gender = jRadioButton2.getText();
      }
    String address = jTextField4.getText();
    String contactNo = jTextField5.getText();
    
    
    Student student = new Student(name, age, studentClass, rollNo, gender, address, contactNo);
    
    // Save to DB
    StudentDaoImpl dao = new StudentDaoImpl();
    dao.addStudent(student);

    // Show confirmation
    javax.swing.JOptionPane.showMessageDialog(this, "Student Registered Successfully!");
    
    jTextField1.setText(""); // Name
    jTextField2.setText(""); // Age
    jComboBox1.setSelectedIndex(0); // Reset class selection
    jTextField3.setText(""); // Roll No
    buttonGroup1.clearSelection(); // Clear gender
    jTextField4.setText(""); // Address
    jTextField5.setText(""); // Contact No

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                                      
    String name = jTextField1.getText();
    int age = Integer.parseInt(jTextField2.getText());
    String studentClass = jComboBox1.getSelectedItem().toString();
    String rollNo = jTextField3.getText();
    String gender = null;
    
    if (jRadioButton1.isSelected()) {
        gender = jRadioButton1.getText();
    } else if (jRadioButton2.isSelected()) {
        gender = jRadioButton2.getText();
    }

    String address = jTextField4.getText();
    String contact = jTextField5.getText();

    Student student = new Student(name, age, studentClass, rollNo, gender, address, contact);

    StudentDaoImpl dao = new StudentDaoImpl();
    dao.updateStudent(student);

    javax.swing.JOptionPane.showMessageDialog(this, "Student Updated Successfully.");


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
                                       
    String rollNo = jTextField3.getText();
    if (rollNo.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Enter Roll No to delete");
        return;
    }
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete?", "Confirm", javax.swing.JOptionPane.YES_NO_OPTION);
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        StudentDaoImpl dao = new StudentDaoImpl();
        dao.deleteStudent(rollNo);
    }

    }//GEN-LAST:event_jButton5ActionPerformed

